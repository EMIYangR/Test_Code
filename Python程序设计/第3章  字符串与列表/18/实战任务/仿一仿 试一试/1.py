print("".join([str(i) for i in range(1,101)]))      #一行代码输出数字 1~100。

print(sum([i for i in range(1,101)]))               #一行代码输出数字 1~100 的和。

print([chr(i) for i in range(48,58)])               #一行代码输出数字 0~9。

print([chr(i) for i in range(65,91)])               #一行代码输出所有大写字母。

print([chr(i) for i in range(97,123)])              #一行代码输出所有小写字母。